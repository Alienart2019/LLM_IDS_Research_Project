"""
dataset_summary.py — LLM-assisted dataset inventory and description.

This module walks the entire data directory, extracts lightweight metadata
from every dataset it finds (CSV, paired Kitsune-style CSV, PCAP, and plain
log files), and hands the collected metadata to the LLM so it can produce a
human-readable summary of what the IDS has been trained on.

Why this exists
---------------
The Trainable IDS can ingest a dozen different dataset formats. Operators
and researchers need a single place to see:

    * which datasets are present,
    * their size (rows / packets / lines),
    * the columns / features each contains,
    * the distribution of normalized labels (benign / suspicious / malicious),
    * a short natural-language description generated by the LLM.

The output is both:

    1. A structured ``DatasetCatalog`` object (for programmatic use), and
    2. A Markdown report written to disk (for humans and documentation).

The LLM call is optional. If ``IDS_USE_LLM`` is disabled the module still
produces a complete structured catalog and a Markdown report — just without
the narrative paragraphs. That way the summarizer is useful even on a box
with no LLM available.

Typical usage
-------------
    from app.dataset_summary import summarize_datasets
    summarize_datasets("data/", output_path="docs/DATASETS.md")

Or from the command line::

    python -m app.summarize_datasets data/
"""

from __future__ import annotations

import json
import logging
from dataclasses import dataclass, field, asdict
from pathlib import Path
from typing import Any, Iterable

import pandas as pd
import requests

from app.config import (
    LLM_API_URL,
    LLM_MODEL,
    USE_LLM,
    SUPPORTED_DATASET_EXTENSIONS,
)
from app.dataloader import (
    LABEL_CANDIDATES,
    _find_column,
    _normalize_label,
)

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Data classes
# ---------------------------------------------------------------------------


@dataclass
class DatasetInfo:
    """
    Structured metadata about a single dataset file (or paired pair).

    Attributes
    ----------
    name:
        Human-readable dataset name (typically the folder or file stem).
    kind:
        One of ``"paired_csv"``, ``"single_csv"``, ``"pcap"``, ``"log"``.
    paths:
        Absolute paths contributing to this dataset. ``paired_csv`` has two
        entries (dataset + labels); everything else has one.
    rows:
        Sampled row / packet / line count. ``None`` if unknown.
    columns:
        Column names discovered in the file. Empty for non-tabular sources.
    label_column:
        Name of the column that holds ground-truth labels, if known.
    label_distribution:
        Mapping of normalized label (benign / suspicious / malicious /
        unknown) to count within the sampled rows.
    sample_rows:
        A few example rows, already converted to plain dicts, to feed the
        LLM so it has concrete context.
    notes:
        Any warnings or problems picked up while scanning.
    llm_description:
        Narrative description produced by the LLM (filled in later).
    """

    name: str
    kind: str
    paths: list[str]
    rows: int | None = None
    columns: list[str] = field(default_factory=list)
    label_column: str | None = None
    label_distribution: dict[str, int] = field(default_factory=dict)
    sample_rows: list[dict[str, Any]] = field(default_factory=list)
    notes: list[str] = field(default_factory=list)
    llm_description: str | None = None


@dataclass
class DatasetCatalog:
    """
    Aggregate result of scanning the whole data directory.

    ``datasets`` is the per-file inventory. ``overall_description`` is an
    LLM-generated paragraph that summarizes what the whole collection
    represents (attack families, data modalities, etc.).
    """

    root: str
    datasets: list[DatasetInfo] = field(default_factory=list)
    overall_description: str | None = None

    def to_dict(self) -> dict[str, Any]:
        """Return the catalog as a nested dict, suitable for JSON output."""
        return {
            "root": self.root,
            "datasets": [asdict(d) for d in self.datasets],
            "overall_description": self.overall_description,
        }


# ---------------------------------------------------------------------------
# Metadata collectors (one per dataset kind)
# ---------------------------------------------------------------------------


# Only the first ``_SAMPLE_ROWS_PER_FILE`` rows are loaded per file. This keeps
# memory flat even on multi-gigabyte Kitsune captures, and is enough context
# for the LLM to describe the shape of the data.
_SAMPLE_ROWS_PER_FILE = 500
_LLM_SAMPLE_ROWS = 5          # rows actually shown to the LLM per dataset
_LLM_TIMEOUT_SECONDS = 30.0


def _count_csv_rows(path: Path) -> int | None:
    """
    Count rows in a CSV without loading it fully into memory.

    Tries ``wc -l`` first (10-50x faster on large files, especially over
    WSL /mnt/c mounts). Falls back to a pure-Python line count, then to
    ``None`` if everything fails. The header line is excluded from the count.
    """
    import shutil
    import subprocess

    if shutil.which("wc"):
        try:
            result = subprocess.run(
                ["wc", "-l", str(path)],
                capture_output=True, text=True, timeout=120, check=True,
            )
            line_count = int(result.stdout.strip().split()[0])
            return max(line_count - 1, 0)
        except Exception as exc:
            logger.debug("wc_row_count_failed", extra={"path": str(path), "error": str(exc)})

    try:
        with open(path, "r", encoding="utf-8", errors="ignore") as fh:
            return max(sum(1 for _ in fh) - 1, 0)
    except Exception as exc:
        logger.debug("csv_row_count_failed", extra={"path": str(path), "error": str(exc)})
        return None


def _collect_single_csv(csv_path: Path) -> DatasetInfo:
    """Build a :class:`DatasetInfo` for a standalone labeled CSV."""
    info = DatasetInfo(
        name=csv_path.stem,
        kind="single_csv",
        paths=[str(csv_path)],
    )
    try:
        sample = pd.read_csv(csv_path, nrows=_SAMPLE_ROWS_PER_FILE, low_memory=False)
    except Exception as exc:
        info.notes.append(f"Failed to read CSV: {exc}")
        return info

    info.columns = [str(c) for c in sample.columns]
    info.rows = _count_csv_rows(csv_path)

    label_col = _find_column(sample, LABEL_CANDIDATES)
    if label_col is None:
        info.notes.append(
            "No label column found. Expected one of: " + ", ".join(LABEL_CANDIDATES)
        )
    else:
        info.label_column = label_col
        info.label_distribution = _label_distribution(sample[label_col])

    info.sample_rows = _take_sample_rows(sample)
    return info


def _collect_paired_csv(dataset_file: Path, labels_file: Path) -> DatasetInfo:
    """
    Build a :class:`DatasetInfo` for a Kitsune-style (features + labels) pair.

    The Kitsune dataset ships with features in ``*_dataset.csv`` and labels
    in a matching ``*_labels.csv``. The labels file is often just an index
    column plus a single column named ``x`` holding 0/1.
    """
    base = dataset_file.name.replace("_dataset.csv", "")
    info = DatasetInfo(
        name=base,
        kind="paired_csv",
        paths=[str(dataset_file), str(labels_file)],
    )

    try:
        dataset_sample = pd.read_csv(
            dataset_file, nrows=_SAMPLE_ROWS_PER_FILE, low_memory=False
        )
        labels_sample = pd.read_csv(
            labels_file, nrows=_SAMPLE_ROWS_PER_FILE, low_memory=False
        )
    except Exception as exc:
        info.notes.append(f"Failed to read pair: {exc}")
        return info

    info.columns = [str(c) for c in dataset_sample.columns]
    info.rows = _count_csv_rows(dataset_file)

    # Find the label column even when it's the awkward "x" used by Kitsune.
    label_col = _find_column(labels_sample, LABEL_CANDIDATES)
    if label_col is None:
        # Accept a single non-index column as the label column (Kitsune case).
        non_index = [
            c for c in labels_sample.columns
            if not str(c).lower().startswith("unnamed")
        ]
        if len(non_index) == 1:
            label_col = non_index[0]
        elif len(labels_sample.columns) == 1:
            label_col = labels_sample.columns[0]

    if label_col is None:
        info.notes.append(
            "Could not determine label column in labels file; "
            f"columns were {list(labels_sample.columns)}."
        )
    else:
        info.label_column = str(label_col)
        info.label_distribution = _label_distribution(labels_sample[label_col])

    info.sample_rows = _take_sample_rows(dataset_sample)
    return info


def _collect_pcap(pcap_path: Path) -> DatasetInfo:
    """
    Build a :class:`DatasetInfo` for a PCAP/PCAPNG capture.

    Only the first ``_SAMPLE_ROWS_PER_FILE`` packets are parsed so we don't
    blow out memory on multi-GB captures used only for reference.
    """
    info = DatasetInfo(
        name=pcap_path.stem,
        kind="pcap",
        paths=[str(pcap_path)],
    )
    try:
        from scapy.all import rdpcap  # Imported lazily — scapy is heavy.

        packets = rdpcap(str(pcap_path), count=_SAMPLE_ROWS_PER_FILE)
        info.rows = len(packets)
        summaries = []
        for pkt in packets[:_LLM_SAMPLE_ROWS]:
            try:
                summaries.append({"summary": pkt.summary()})
            except Exception:
                continue
        info.sample_rows = summaries
        info.notes.append(
            "PCAPs are sampled for summary only; they are not used for training."
        )
    except Exception as exc:
        info.notes.append(f"Failed to read pcap: {exc}")
    return info


def _collect_log(log_path: Path) -> DatasetInfo:
    """Build a :class:`DatasetInfo` for plain-text log files."""
    info = DatasetInfo(
        name=log_path.stem,
        kind="log",
        paths=[str(log_path)],
    )
    try:
        lines: list[str] = []
        with open(log_path, "r", encoding="utf-8", errors="ignore") as fh:
            for idx, line in enumerate(fh):
                if idx >= _SAMPLE_ROWS_PER_FILE:
                    break
                line = line.rstrip("\n")
                if line:
                    lines.append(line)
        info.rows = len(lines)
        info.sample_rows = [{"line": ln} for ln in lines[:_LLM_SAMPLE_ROWS]]
    except Exception as exc:
        info.notes.append(f"Failed to read log: {exc}")
    return info


