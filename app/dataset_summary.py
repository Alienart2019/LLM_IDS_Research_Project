# Scanner
# ---------------------------------------------------------------------------


def scan_datasets(root_path: str | Path) -> DatasetCatalog:
    """
    Walk ``root_path`` and collect metadata for every dataset found.

    The scanner understands the same sources as the training pipeline:

    * Paired ``*_dataset.csv`` / ``*_labels.csv`` files (Kitsune style).
    * Standalone labeled CSVs.
    * ``*.pcap`` / ``*.pcapng`` captures (summary only — not used for training).
    * ``*.log`` / ``*.txt`` text log files.

    Unknown extensions are ignored silently.

    Parameters
    ----------
    root_path:
        Path to the data directory. May be a string or a ``Path``.

    Returns
    -------
    DatasetCatalog
        Populated catalog. The ``llm_description`` / ``overall_description``
        fields are still ``None`` at this point — call
        :func:`enrich_with_llm` to populate them.
    """
    root = Path(root_path).expanduser().resolve()
    catalog = DatasetCatalog(root=str(root))

    if not root.exists():
        raise FileNotFoundError(f"Data directory not found: {root}")

    processed: set[Path] = set()

    # 1. Paired Kitsune-style CSVs first (so we don't double-count their
    #    _dataset.csv as a single_csv on the second pass).
    for dataset_file in sorted(root.rglob("*_dataset.csv")):
        base_name = dataset_file.name.replace("_dataset.csv", "")
        folder = dataset_file.parent
        candidates = [
            folder / f"{base_name}_labels.csv",
            folder / f"{base_name.lower()}_labels.csv",
            folder / f"{base_name.upper()}_labels.csv",
        ]
        labels_file = next((c for c in candidates if c.exists()), None)
        if labels_file is None:
            matches = list(folder.glob("*_labels.csv"))
            if len(matches) == 1:
                labels_file = matches[0]

        if labels_file is None:
            continue

        catalog.datasets.append(_collect_paired_csv(dataset_file, labels_file))
        processed.add(dataset_file)
        processed.add(labels_file)

    # 2. Remaining CSVs as single-file datasets.
    for csv_file in sorted(root.rglob("*.csv")):
        if csv_file in processed:
            continue
        if csv_file.name.endswith("_labels.csv"):
            continue
        catalog.datasets.append(_collect_single_csv(csv_file))

    # 3. PCAPs (reference only).
    for ext in ("*.pcap", "*.pcapng"):
        for pcap_file in sorted(root.rglob(ext)):
            catalog.datasets.append(_collect_pcap(pcap_file))

    # 4. Plain text logs.
    for ext in ("*.log", "*.txt"):
        for log_file in sorted(root.rglob(ext)):
            catalog.datasets.append(_collect_log(log_file))

    logger.info(
        "dataset_scan_complete",
        extra={"root": str(root), "dataset_count": len(catalog.datasets)},
    )
    return catalog


