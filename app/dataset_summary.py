# Helpers
# ---------------------------------------------------------------------------


def _label_distribution(series: pd.Series) -> dict[str, int]:
    """
    Apply the same label normalization used during training so the summary
    matches how the model actually sees the data.
    """
    counts: dict[str, int] = {"benign": 0, "suspicious": 0, "malicious": 0, "unknown": 0}
    for raw in series.tolist():
        normalized = _normalize_label(raw)
        counts[normalized if normalized is not None else "unknown"] += 1
    # Drop zero buckets so the report stays tidy.
    return {k: v for k, v in counts.items() if v > 0}


def _take_sample_rows(df: pd.DataFrame) -> list[dict[str, Any]]:
    """
    Grab a handful of rows, stringify them, and return them as plain dicts.

    Wide tables (Kitsune has 100+ numeric feature columns) get truncated to
    keep the LLM prompt small.
    """
    head = df.head(_LLM_SAMPLE_ROWS).copy()

    # Truncate very wide tables so the LLM prompt doesn't explode.
    max_cols = 12
    if len(head.columns) > max_cols:
        keep = list(head.columns[: max_cols - 1]) + [head.columns[-1]]
        head = head[keep]

    rows: list[dict[str, Any]] = []
    for _, row in head.iterrows():
        rows.append({str(k): _stringify(v) for k, v in row.items()})
    return rows


def _stringify(value: Any) -> str:
    """Coerce any scalar into a short, safe string for prompt inclusion."""
    try:
        text = str(value)
    except Exception:
        return "<unreadable>"
    if len(text) > 120:
        return text[:117] + "..."
    return text


