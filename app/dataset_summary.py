_PER_DATASET_PROMPT = """\
You are a cybersecurity data analyst.

Describe the following dataset in 3-5 sentences for a technical reader. Cover:
  - what kind of data it contains (network traffic, auth logs, etc.)
  - likely use for intrusion detection training
  - anything notable about the label distribution
  - any data-quality concerns from the sample rows

Be concise and factual. Do not invent details that are not visible in the
metadata below.

Dataset metadata (JSON):
{metadata}
"""


_OVERALL_PROMPT = """\
You are a cybersecurity data analyst.

The following catalog describes every dataset currently available to a
defensive Intrusion Detection System (IDS). Write a 2-paragraph executive
summary that a reader new to the project could use to understand:
  1. The overall scope: which attack families and data modalities are represented.
  2. Any gaps or imbalances the team should be aware of.

Be concise. Do not invent details not present in the catalog.

Catalog (JSON):
{catalog}
"""


def _call_llm(prompt: str) -> str:
    """
    Send a prompt to the configured LLM endpoint and return the response text.

    On any failure (network error, bad status, malformed JSON) an empty
    string is returned — callers should treat that as "no LLM output" and
    continue gracefully.
    """
    payload = {"model": LLM_MODEL, "prompt": prompt, "stream": False}
    try:
        resp = requests.post(LLM_API_URL, json=payload, timeout=_LLM_TIMEOUT_SECONDS)
        resp.raise_for_status()
        return (resp.json().get("response") or "").strip()
    except Exception as exc:
        logger.warning("llm_call_failed", extra={"error": str(exc)})
        return ""


def enrich_with_llm(catalog: DatasetCatalog) -> DatasetCatalog:
    """
    Populate ``llm_description`` on each dataset and ``overall_description``
    on the catalog itself.

    If ``IDS_USE_LLM`` is false this is a no-op (the catalog is returned
    unchanged) — useful for air-gapped deployments.
    """
    if not USE_LLM:
        logger.info("llm_disabled_skipping_enrichment")
        return catalog

    # Per-dataset narratives.
    for ds in catalog.datasets:
        metadata = json.dumps(
            {
                "name": ds.name,
                "kind": ds.kind,
                "rows": ds.rows,
                "columns": ds.columns[:30],
                "label_column": ds.label_column,
                "label_distribution": ds.label_distribution,
                "sample_rows": ds.sample_rows,
                "notes": ds.notes,
            },
            indent=2,
            default=str,
        )
        description = _call_llm(_PER_DATASET_PROMPT.format(metadata=metadata))
        if description:
            ds.llm_description = description

    # Whole-catalog narrative — we send a compacted view to keep tokens down.
    compact = {
        "root": catalog.root,
        "datasets": [
            {
                "name": d.name,
                "kind": d.kind,
                "rows": d.rows,
                "label_distribution": d.label_distribution,
            }
            for d in catalog.datasets
        ],
    }
    overall = _call_llm(
        _OVERALL_PROMPT.format(catalog=json.dumps(compact, indent=2, default=str))
    )
    if overall:
        catalog.overall_description = overall

    return catalog


# ---------------------------------------------------------------------------
# Markdown rendering
# ---------------------------------------------------------------------------


def render_markdown(catalog: DatasetCatalog) -> str:
    """
    Render a :class:`DatasetCatalog` to a Markdown report.

    The report is self-contained and designed to be committed into the
    repository (e.g. at ``docs/DATASETS.md``) so newcomers can quickly see
    what data the IDS has been trained on.
    """
    lines: list[str] = []
    lines.append("# Dataset Catalog\n")
    lines.append(f"*Scanned root:* `{catalog.root}`\n")
    lines.append(f"*Dataset count:* **{len(catalog.datasets)}**\n")

    if catalog.overall_description:
        lines.append("\n## Overall Summary\n")
        lines.append(catalog.overall_description + "\n")

    # Compact overview table.
    lines.append("\n## Index\n")
    lines.append("| # | Name | Kind | Rows | Labels |")
    lines.append("|---|------|------|------|--------|")
    for i, ds in enumerate(catalog.datasets, start=1):
        label_summary = (
            ", ".join(f"{k}={v}" for k, v in ds.label_distribution.items())
            if ds.label_distribution
            else "—"
        )
        rows = ds.rows if ds.rows is not None else "?"
        lines.append(f"| {i} | {ds.name} | {ds.kind} | {rows} | {label_summary} |")

    # Per-dataset detail sections.
    lines.append("\n## Per-Dataset Detail\n")
    for i, ds in enumerate(catalog.datasets, start=1):
        lines.append(f"\n### {i}. {ds.name}  _({ds.kind})_\n")
        lines.append("**Paths:**")
        for p in ds.paths:
            lines.append(f"- `{p}`")
        lines.append(f"\n**Rows:** {ds.rows if ds.rows is not None else 'unknown'}")
        if ds.label_column:
            lines.append(f"**Label column:** `{ds.label_column}`")
        if ds.label_distribution:
            dist = ", ".join(f"`{k}`: {v}" for k, v in ds.label_distribution.items())
            lines.append(f"**Label distribution (sampled):** {dist}")
        if ds.columns:
            shown = ds.columns[:15]
            more = "" if len(ds.columns) <= 15 else f" _(+{len(ds.columns) - 15} more)_"
            lines.append(f"**Columns:** {', '.join(f'`{c}`' for c in shown)}{more}")
        if ds.notes:
            lines.append("\n**Notes:**")
            for n in ds.notes:
                lines.append(f"- {n}")
        if ds.llm_description:
            lines.append("\n**LLM description:**\n")
            lines.append(ds.llm_description)

    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------------------
# Top-level entry point
# ---------------------------------------------------------------------------


def summarize_datasets(
    root_path: str | Path,
    output_path: str | Path | None = "docs/DATASETS.md",
    *,
    use_llm: bool | None = None,
) -> DatasetCatalog:
    """
    End-to-end convenience wrapper: scan, enrich, render.

    Parameters
    ----------
    root_path:
        Directory to scan (typically ``data/``).
    output_path:
        Where to write the Markdown report. If ``None``, no file is written.
    use_llm:
        Override the ``IDS_USE_LLM`` environment toggle for this run.
        ``None`` (default) honors the environment setting.

    Returns
    -------
    DatasetCatalog
        The fully populated catalog. The same information has also been
        written to ``output_path`` as Markdown if that argument was supplied.
    """
    catalog = scan_datasets(root_path)

    # Allow per-call override of the USE_LLM flag without mutating config.
    global USE_LLM  # noqa: PLW0603 — intentional, temporary override
    if use_llm is not None:
        previous = USE_LLM
        USE_LLM = use_llm
        try:
            enrich_with_llm(catalog)
        finally:
            USE_LLM = previous
    else:
        enrich_with_llm(catalog)

    if output_path is not None:
        out = Path(output_path)
        out.parent.mkdir(parents=True, exist_ok=True)
        out.write_text(render_markdown(catalog), encoding="utf-8")
        logger.info("dataset_summary_written", extra={"path": str(out)})

    return catalog
